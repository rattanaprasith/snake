<p><b>Vad är ormspel 1?</b>
<br><p>Ormspel 1 är ett litet, enkelt canvas spel där spelaren ska styra "ormen" för att äta upp maten som är utlagd dvs. blåa punkten. 
Ormen styrs med hjälp av pilknapparna på tangentbordet och spelaren får en poäng per uppäten matbit. Spelet slutar då ormen kolliderar mot väggen.</p>
<p>Spelet är skapat av Rattana Prasith som läser kurspaketet "Databaser, HTML, CSS, JavaScript och PHP" vid Blekinge tekniska högskola. Spelet är gjort med hjälp av den här <a href="http://cssdeck.com/labs/classic-snake-game-with-html5-canvas">tutorialen</a></p>
<br>

<p><b>Produktanalys</b>
<br><p>Ormspelet 1 kan vara i sig en likelse till det klassiska orm spelet. Spelet har samma principer men skillnaden är att det klassiska spelet innehåller fler funktioner än ormspelet 1. Någon som man tänker på är att många spel bygger på samma grundidéen men de kan se annorlunda ut vilket får det hela kännas som ett helt nytt spel. Spelaren är bekant med speltypen och vet direkt vad som är målet med spelet.</p>

